    private void btnSearchInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchInfoActionPerformed
        // TODO add your handling code here:
        Search = txtSearch.getText().trim();
        Type = buttonGroup1.getSelection().toString();
        if (Search.equals("")) {
            JOptionPane.showMessageDialog(null, "Search field is null!");
        } else {
            try {
                //clean table
                DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
                model1.removeRow(0);
                //show table
                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                Connection cn = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=LoanProcessingSystem", "sa", "123456");
                Statement s = cn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                if (chkLoanID.isSelected()) {
                    rs = s.executeQuery("SELECT * FROM tblLoanProcessing WHERE LoanID ='" + Search + "'");
                }
                if (chkAmount.isSelected()) {
                    rs = s.executeQuery("SELECT * FROM tblLoanProcessing WHERE Amount ='" + Search + "'");
                }
                if (chkDuration.isSelected()) {
                    rs = s.executeQuery("SELECT * FROM tblLoanProcessing WHERE Duration ='" + Search + "'");
                }
                while (rs.next()) {
                    model1.addRow(new Object[]{rs.getInt(1), rs.getInt(3), rs.getDate(5), rs.getInt(4), rs.getInt(6), rs.getBoolean(7)});
                }
                if (model1.getRowCount() == 0) {
                    JOptionPane.showMessageDialog(null, "Can't Find");
                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(SearchForm.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(UserForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSearchInfoActionPerformed


    public boolean checkAmount() {
        salary = Integer.parseInt(txtSalary.getText());
        float amount = Float.parseFloat(txtAmount.getText());
        int years = 12;
        switch (cbxDuration.getSelectedItem().toString()) {
            case "1":
                years = 12;
                break;
            case "2":
                years = 24;
                break;
            case "3":
                years = 36;
                break;
            case "4":
                years = 48;
                break;
        }
        if ((years * salary) > (amount * 1.5)) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "(Amount * 1.5)  must be lower than (Duration * Salary in half)");
            return false;
        }
    }


    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            if (txtFullName.getText().equals("") || txtIdentity.getText().equals("")
                    || txtCusAdd.getText().equals("") || txtSalary.getText().equals("")
                    || txtJob.getText().equals("") || txtComName.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "All fields is not blank");
            } else {
                if (checkIdentity() == true) {
                    Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                    Connection con = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=LoanProcessingSystem", "sa", "123456");
                    Statement stt = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
                    ResultSet rs = stt.executeQuery("SELECT * FROM tblCustomer");
                    rs.moveToInsertRow();
                    rs.updateString("CustomerName", txtFullName.getText());
                    rs.updateString("IdentityID", txtIdentity.getText());
                    rs.updateString("CustomerAddress", txtCusAdd.getText());
                    rs.updateString("Salary", txtSalary.getText());
                    rs.updateString("Job", txtJob.getText());
                    rs.updateString("CompanyName", txtComName.getText());
                    rs.updateString("Gender", "Male");
                    rs.insertRow();
                    JOptionPane.showMessageDialog(null, "Save Success");
                    //set field
                    txtFullName.setEditable(false);
                    txtCusAdd.setEditable(false);
                    txtIdentity.setEditable(false);
                    txtSalary.setEditable(false);
                    txtJob.setEditable(false);
                    txtComName.setEditable(false);
                    txtFullName.setText("");
                    txtCusAdd.setText("");
                    txtIdentity.setText("");
                    txtSalary.setText("");
                    txtJob.setText("");
                    txtComName.setText("");
                    //set button
                    jButton1.setEnabled(false);
                    jButton2.setEnabled(true);
                    jButton3.setEnabled(false);
                    jButton4.setEnabled(false);
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Incomplete");
        }
    }//GEN-LAST:event_jButton3ActionPerformed


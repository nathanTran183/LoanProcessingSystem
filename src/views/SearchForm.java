    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            // TODO add your handling code here:            
            String find = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
            rs.absolute(0);
            while (rs.next()) {
                String f = rs.getString("LoanID");
                if (f.equals(find)) {
                    Cus = rs.getInt("CustomerID") + "";
                }
            }
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection cn = DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=LoanProcessingSystem", "sa", "123456");
            Statement s = cn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            rt = s.executeQuery("SELECT * FROM tblCustomer WHERE CustomerID = '" + Cus + "'");
            while (rt.next()) {
                txtCusID.setText(rt.getString("CustomerID"));
                txtCusName.setText(rt.getString("CustomerName"));
                txtIdentity.setText(rt.getString("IdentityID"));
                txtAdd.setText(rt.getString("CustomerAddress"));
                txtDOB.setText(rt.getString("DateOfBirth"));
                if (rt.getString("Gender").equals("Male")) {
                    chkMale.setSelected(true);
                } else {
                    chkFemale.setSelected(true);
                }
                txtTel.setText(rt.getString("Telephone"));
                txtJob.setText(rt.getString("Job"));
                txtSalary.setText(rt.getString("Salary"));
                txtComName.setText(rt.getString("CompanyName"));
                txtEmail.setText(rt.getString("Email"));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SearchForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UserForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable1MouseClicked

